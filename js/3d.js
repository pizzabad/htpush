	var V = function (obj) { return document.getElementById(obj); }
	V("back").onclick=function(){
		var self=this;
		self.className="fliped";	
		V("front").className="flip";
 
		setTimeout(function(){
			V("front").className="fliped";
			self.className="flip";
		},1000);
	}
	
